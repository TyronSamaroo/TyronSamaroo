def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--out", default=os.path.join(os.path.dirname(__file__), "..", "variations"))
    args = ap.parse_args()
    out = os.path.abspath(args.out)
    os.makedirs(out, exist_ok=True)
    for fname, fn in THEMES.items():
        path = os.path.join(out, fname)
        with open(path, "w") as f:
            f.write(fn())
        print(f"wrote {path}")


if __name__ == "__main__":
    main()
