# ---------- URL helpers (all the SVG services) ---------------------------

def capsule(**params):
    base = "https://capsule-render.vercel.app/api"
    q = "&".join(f"{k}={quote(str(v), safe='')}" for k, v in params.items())
    return f"{base}?{q}"


def typing(lines, **params):
    # readme-typing-svg.demolab.com
    params.setdefault("font", "Fira+Code")
    params["lines"] = ";".join(lines)
    base = "https://readme-typing-svg.demolab.com"
    q = "&".join(f"{k}={quote(str(v), safe='+;,')}" for k, v in params.items())
    return f"{base}?{q}"


def shield(label, color, logo=None, logo_color=None, style="for-the-badge"):
    # img.shields.io static badge
    s = f"https://img.shields.io/badge/{quote(label)}-{color}?style={style}"
    if logo:
        s += f"&logo={logo}"
    if logo_color:
        s += f"&logoColor={logo_color}"
    return s


def skillicons(theme="dark", perline=13, icons=None):
    icons = icons or C["skillicons_slug"]
    return f"https://skillicons.dev/icons?i={icons}&theme={theme}&perline={perline}"


